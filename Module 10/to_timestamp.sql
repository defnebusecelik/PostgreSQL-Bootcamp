-- to_timestamp() function

SELECT TO_TIMESTAMP(
	'2020-10-28 10:30:25',
	'YYYY-MM-DD HH:MI:SS'
); --"2020-10-28 10:30:25+03"

SELECT TO_TIMESTAMP(
	'2020		May',
	'YYYY MON'
); --"2020-05-01 00:00:00+03"

SELECT TO_TIMESTAMP(
	'2020-01-01 23:8:00',
	'YYYY-MM-DD HH24:MI:SS'
); --"2020-01-01 23:08:00+03"

