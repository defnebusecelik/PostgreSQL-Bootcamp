-- to_number() function

SELECT TO_NUMBER('1420.89','9999.9');  --1420.8

SELECT TO_NUMBER('10,625.78-','99G999D99S');

SELECT TO_NUMBER('$1,420.64','L9G999');  --1420

SELECT TO_NUMBER('1,234,567.89','9G999g99'); --123456

SELECT TO_NUMBER('1,234,567,89','9G999g999D99'); --1234567.89

SELECT TO_NUMBER('$1,978,299.78','L9G999g999.99'); --1978299.78